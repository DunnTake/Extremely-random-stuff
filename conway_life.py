    print()
    for y in range(ylength):
        for x in range(xlength):
            result = check_neighbors(y,x)
            
            if result == "death":
                gcopy[y][x] = 0
            elif result == "reborn" and gcopy[y][x] == 0:
                gcopy[y][x] = 1
    
    grid = [row[:] for row in gcopy]
    time.sleep(0.1)
